# Ask the user for their current age
current_age = int(input("What is your current age? "))

# Ask the user how many years they want to travel back in time
years_travel = int(input("How many years do you want to travel back in time? "))

# Calculate the age after time travel
age_after_travel = current_age - years_travel

# Print the age after time travel
print("After traveling back in time, you will be", age_after_travel, "years old.")

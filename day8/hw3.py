# Variables of different types
integer_var = 10
float_var = 3.14
string_var = "Hello, world!"
boolean_var = True
list_var = [1, 2, 3]
tuple_var = (4, 5, 6)
dictionary_var = {"name": "Alice", "age": 30}

# Print the data types of the variables
print("Data types of variables:")
print("integer_var:", type(integer_var))
print("float_var:", type(float_var))
print("string_var:", type(string_var))
print("boolean_var:", type(boolean_var))
print("list_var:", type(list_var))
print("tuple_var:", type(tuple_var))
print("dictionary_var:", type(dictionary_var))

# Creating a list with different types of data
my_list = [10, 'apple', 3.14, True, [1, 2, 3], {'name': 'John', 'age': 30}]

# Displaying the original list
print("Original list:")
print(my_list)

# Replacing elements in the list
my_list[1] = 'banana'  # Replace 'apple' with 'banana'
my_list[3] = False     # Replace True with False
my_list[5] = 'Jane'    # Replace the dictionary with a string

# Displaying the modified list
print("\nModified list:")
print(my_list)